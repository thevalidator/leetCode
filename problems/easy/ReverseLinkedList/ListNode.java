package com.leetcode.problems.easy.ReverseLinkedList;

/**
 * @author theValidator <the.validator@yandex.ru>
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
